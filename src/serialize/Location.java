package serialize;

public class Location {
    private double lat;
    private double lng;

    public int getLat() {
        return lat;
    }

    public int getLng() {
        return lng;
    }

    public void setLat(int lat) {
        this.lat = lat;
    }

    public void setLng(int lng) {
        this.lng = lng;
    }
}
